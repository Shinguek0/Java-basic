package basico;

public class While_For {

	public static void main(String[] args) {
		
		int contador = 1;
		while(contador <= 10) {
			System.out.println(contador);
			contador++;
		}
		
		System.out.println("---");
		
		for(int i = 0; i <= 20; i+=2) {
			System.out.println(i);
			
		}

	}

}
